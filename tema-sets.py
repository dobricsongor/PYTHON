set1 = {1,3,4,5,8,12,20,25,30,33}
set2 = {1,2,5,10,12,20,28,31,33,35}

print('#union (combine the two sets)')
print(set1 | set2)

print('#intersection (only the common values)')
print(set1 & set2)

print('#difference (everything in set1 but not in set2)')
print(set1 - set2)

print('#symmetric difference (combine the two sets, but without the common values)')
print(set1 ^ set2)

